    //展示是否有数据
    private static Toast mShortToast;
    private static Toast mLongToast;
    public static void ToastShow(Context context, Boolean isshow, String message){
        if(isshow){
            if (mShortToast == null) {
                mShortToast = Toast.makeText(context, message, 0);
            }
            mShortToast.setText(message);
            mShortToast.show();
        }
    }
